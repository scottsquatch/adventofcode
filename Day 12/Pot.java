public class Pot
{
	private int num;
	private boolean isFilled;

	public Pot(int num)
	{
		this.num = num;
	}

	public int getNum()
	{
		return this.num;
	}

	public boolean getIsFilled()
	{
		return this.isFilled;
	}

	public void setIsFilled(boolean isFilled)
	{
		this.isFilled = isFilled;
	}

	@Override
	public String toString()
	{
		if (isFilled)
		{
			return "#";
		}
		else
		{
			return ".";
		}
	}
}
